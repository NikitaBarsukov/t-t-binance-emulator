CREATE TABLE IF NOT EXISTS USER_ORDER
(
    ID              BIGSERIAL,
    SESSION_ID      VARCHAR(255),
    AVG_PRICE       VARCHAR(255),
    CLIENT_ORDER_ID VARCHAR(255),
    CUM_QUOTE       VARCHAR(255),
    EXECUTED_QTY    VARCHAR(255),
    ORDER_ID        BIGINT,
    ORIG_QTY        VARCHAR(255),
    ORIG_TYPE       VARCHAR(255),
    PRICE           VARCHAR(255),
    REDUCE_ONLY     BOOLEAN,
    SIDE            VARCHAR(255),
    POSITION_SIDE   VARCHAR(255),
    STATUS          VARCHAR(255),
    STOP_PRICE      VARCHAR(255),
    CLOSE_POSITION  BOOLEAN,
    SYMBOL          VARCHAR(255),
    TIME            TIMESTAMP,
    TIME_IN_FORCE   VARCHAR(255),
    TYPE            VARCHAR(255),
    ACTIVATE_PRICE  VARCHAR(255),
    PRICE_RATE      VARCHAR(255),
    UPDATE_TIME     TIMESTAMP,
    WORKING_TYPE    VARCHAR(255),
    PRICE_PROTECT   BOOLEAN,
    UNIQUE (ID)
)